package it.unibo.common.tile;

/**
 * TerrainType.
 */
// CHECKSTYLE: JavadocVariable OFF
public enum TerrainType {
    /**
     * Desert, produces nothing.
     */
    DESERT,
    /**
     * Field, produces grain.
     */
    FIELD,
    /**
     * Forest, produces lumber.
     */
    FOREST,
    /**
     * Hill, produces brick.
     */
    HILL,
    /**
     * Mountain, produces ore.
     */
    MOUNTAIN,
    /**
     * Pasture, produces wool.
     */
    PASTURE;
}
