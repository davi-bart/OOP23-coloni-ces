package it.unibo.common.road;

/**
 * Direction of a road regarding the hexagonal tile. The possible directions
 * represent the directions of an edge of the hexagon.
 */
public enum RoadDirection {
    /**
     * Up right.
     */
    UPRIGHT,
    /**
     * Right.
     */
    RIGHT,
    /**
     * Down right.
     */
    DOWNRIGHT,
    /**
     * Down left.
     */
    DOWNLEFT,
    /**
     * Left.
     */
    LEFT,
    /**
     * Up left.
     */
    UPLEFT
}
