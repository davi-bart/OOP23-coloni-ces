package it.unibo.common.property;

/**
 * Direction of a property regarding the hexagonal tile. The possible directions
 * represent the directions of a vertex of the hexagon.
 */
public enum PropertyDirection {
    /**
     * Up.
     */
    UP,
    /**
     * Up right.
     */
    UPRIGHT,
    /**
     * Down right.
     */
    DOWNRIGHT,
    /**
     * Down.
     */
    DOWN,
    /**
     * Down left.
     */
    DOWNLEFT,
    /**
     * Up left.
     */
    UPLEFT
}
